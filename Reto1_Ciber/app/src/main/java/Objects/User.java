package Objects;

public class User {

    private int user_Id;
    private String user_dni;
    private String user_name;
    private String user_surname;
    private String user_mail;
    private String user_pass;
    private int user_idDep;
    private int user_idRol;

    public int getUser_Id() {
        return user_Id;
    }
    public void setUser_Id(int user_Id) {
        this.user_Id = user_Id;
    }
    public String getUser_dni() {
        return user_dni;
    }
    public void setUser_dni(String user_dni) {
        this.user_dni = user_dni;
    }
    public String getUser_name() {
        return user_name;
    }
    public void setUser_name(String user_name) {
        this.user_name = user_name;
    }
    public String getUser_surname() {
        return user_surname;
    }
    public void setUser_surname(String user_surname) {
        this.user_surname = user_surname;
    }
    public String getUser_mail() {
        return user_mail;
    }
    public void setUser_mail(String user_mail) {
        this.user_mail = user_mail;
    }
    public String getUser_pass() {
        return user_pass;
    }
    public void setUser_pass(String user_pass) {
        this.user_pass = user_pass;
    }
    public int getUser_idDep() {
        return user_idDep;
    }
    public void setUser_idDep(int user_idDep) {
        this.user_idDep = user_idDep;
    }
    public int getUser_idRol() {
        return user_idRol;
    }
    public void setUser_idRol(int user_idRol) {
        this.user_idRol = user_idRol;
    }



}
